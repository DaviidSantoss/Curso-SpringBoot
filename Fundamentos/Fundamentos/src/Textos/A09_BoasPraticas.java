	/*
	 * Paginação:
	 * 
	 * "Dividir grandes volumes de dados em “páginas” menores para não sobrecarregar cliente e servidor."
	 * Você não lê um livro de 500 páginas de uma vez, você vira página por página.
	 * 
	 * Filtros:
	 * 
	 * Permitir que o cliente refine os dados retornados com critérios específicos. Buscar roupa por
	 * tamanho e cor numa loja online
	 * 
	 * Definir Recurso lógicos:
	 * 
	 * Modelar URIs como substantivos que representam coisas do domínio, não ações. Placas de rua com
	 * nomes, não instruções.
	 * 
	 * Tolerância a falhas:
	 * 
	 * Garantir que o sistema continue funcionando mesmo quando algo falha.Estepe no carro,você não para
	 * a viagem por causa de um pneu furado.
	 * 
	 * cache:
	 * 
	 * Guardar respostas prontas para evitar processamento repetido. Tipo guardar comida pronta na
	 * geladeira esquenta em vez de cozinhar tudo de novo.
	 * 
	 * Conectividade:
	 * 
	 * Garantir comunicação eficiente e estável entre serviços e clientes. tipo sinal de Wi-Fi forte
	 * ,sem isso, nada funciona direito.
	 * 
	 * Timeouts:
	 * 
	 * Limitar quanto tempo o sistema espera por uma resposta antes de desistir. Como Esperar alguém
	 * atrasado,você não espera pra sempre.
	 * 
	 * documentação:
	 * 
	 * Explicar como a API funciona para que outros consigam usá-la sem perguntar. Tipo Manual de
	 * instruções.
	 * 
	 * Utilizar ssl:
	 * 
	 * Criptografar a comunicação para proteger dados em trânsito.
	 * 
	 * Versionamento:
	 * 
	 * Manter versões diferentes da API para não quebrar clientes antigos.
	 * 
	 * Teste e validação:
	 * 
	 * Garantir que a API funciona corretamente e rejeita dados inválidos.
	 * 
	 * Self-Service:
	 * 
	 * Permitir que o próprio usuário resolva problemas sem suporte manual.
	 * 
	 * Exportações:
	 *
	 * Permitir que dados da API sejam baixados em formatos utilizáveis (CSV, PDF, Excel, etc.).
	 *
	 * I18n/Globalization:
	 *
	 * Adaptar mensagens e formatos para idioma e região do usuário.
	 *
	 * Notificações:
	 *
	 * Avisar usuários ou sistemas sobre eventos importantes.
	 *
	 * Limite de Campos:
	 * 
	 * Restringir quais e quantos campos podem ser enviados ou retornados.
	 *
	 * Monitorar a API:
	 * 
	 * Acompanhar saúde, erros e performance da API em tempo real.
	 */

