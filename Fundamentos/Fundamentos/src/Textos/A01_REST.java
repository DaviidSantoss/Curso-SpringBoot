	/*
	 * "Representational State Transfer (REST) é um estilo de arquitetura de software para sistemas
	 * distribuidos de hipermídia, como a word wide web.
	 */

	/*
	 * 1- Cliente-Servidor: Clientes e servidores são separados.
	 * 
	 * 2-Stateless server: O servidor não deve guardar o estado do cliente.cada requeste de um cliente
	 * contém todas as informações necessárias para atende-la.
	 * 
	 * 3-Cacheable: O cliente deve ser informado sobre as propriedades de cache de um recurso para que
	 * ele possa decidir quando deve ou não usar o cache.
	 * 
	 * 4- Interface Uniforme: Existe uma interface uniforme entre cliente e servidor.
	 * 
	 * 5-Sistema em camadas: Deve suportar conceitos como balanceamento de carga proxies e firewalls..
	 * 
	 * 6- Código sobre demanda: O cliente pode solicitar o código do servidor
	 * 
	 */

